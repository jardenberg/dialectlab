function getLocaleFromPath(pathname = '/') {
  const [firstSegment] = String(pathname).split('?')[0].split('/').filter(Boolean);
  if (SUPPORTED_LOCALES.includes(firstSegment)) {
    return firstSegment;
  }

  return DEFAULT_LOCALE;
}

function getLocalePath(locale) {
  return locale === DEFAULT_LOCALE ? '/' : `/${locale}`;
}

function getCanonicalUrl(locale) {
  return `${BASE_URL}${getLocalePath(locale)}`;
}

function getAlternateLinks() {
  return SUPPORTED_LOCALES.map((locale) => ({
    locale,
    href: getCanonicalUrl(locale),
  }));
}

function getLocaleBundle(locale = DEFAULT_LOCALE) {
  return localeBundles[locale] || localeBundles[DEFAULT_LOCALE];
}

